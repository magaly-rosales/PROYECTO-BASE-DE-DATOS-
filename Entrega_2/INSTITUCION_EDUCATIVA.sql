CREATE TABLE CURSO (
    ID_CURSO NUMBER(5) PRIMARY KEY,
    NOMBRE_CURSO VARCHAR2(30) NOT NULL UNIQUE,
    CREDITO NUMBER(10,2) NOT NULL,
    CONSTRAINT CHK_CREDITO CHECK (CREDITO > 0)
);


